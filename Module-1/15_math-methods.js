// 1. Math.abs()
console.log(Math.abs(-100));

// 2. Math.PI
console.log(Math.PI);

// 3. Math.floor()
let cgpa = 3.5;
console.log(Math.floor(cgpa));

// 4. Math.ceil()
console.log(Math.ceil(cgpa));

// 5. Math.round()
console.log(Math.round(cgpa));

// 6. Math.random(), Math.toFixed()
let randomNumber = Math.random() * 10;
console.log(randomNumber.toFixed(0));

// 7. Math.max()
console.log(Math.max(1, 3, 4, 5, 7, 8, 9));

// 8. Math.min()
console.log(Math.min(1, 3, 4, 5, 7, 8, 9));

// 9. Math.sqrt()
console.log(Math.sqrt(64));
